</p>

<h3>9. 投資人民幣計價產品的風險</h3>

<p>9.1 投資風險/市場風險</p>

<p>人民幣產品須面對投資風險，並且可能不保本。即產品內的投資或相關資產的價格可升可跌，而導致產
    品可能賺取收益或招致損失。因此，即使人民幣升值，你亦可能須承受虧損。視乎該人民幣產品的性質
    及投資目標，你可能須承受其他風險。作出投資決定前，你應清楚瞭解產品的性質、投資目標、策略、
    主要特點及風險，評估有關產品是否符合你的投資需要，並考慮你是否可以承受有關風險。如有疑問，
    應尋求專業意見。
</p>

<p>9.2 流通風險</p>

<p>由於人民幣產品是一項新產品，因此可能沒有一般的交易活動或活躍的二手市場。因此，你或不能即時
    出售有關產品，又或可能要以極低價出售。</p>

<p>9.3 發行人/交易對手風險</p>

<p>人民幣產品須面對發行人的信貸風險及無力償債風險。你應該仔細考慮發行人的信用程度，再作出投資
    決定。由於人民幣產品亦可能投資於衍生工具，你亦須承受衍生工具發行人違約的風險。這些風險可能
    對產品的回報有負面影響，更可能構成重大損失。</p>

<p>9.4 貨幣風險</p>
<p>一般來說，非內地（包括香港）的投資者若以人民幣以外的本地貨幣投資人民幣產品，便需承受匯率風
    險。因為人民幣是受到外匯管制的貨幣，當你打算投資於人民幣產品時，便可能要將你的本地貨幣轉換
    為人民幣。而當你贖回或售出你的投資時，你或需要將人民幣轉換回本地貨幣（即使贖回或出售投資的
    收益是以人民幣繳付）。在這過程中，你會牽涉轉換貨幣的成本，亦要承受匯率風險。換言之，就算你
    買賣該人民幣產品的價格不變，於轉換貨幣的過程中，如果人民幣貶值，你亦會有所損失。正如所有貨
    幣一樣，人民幣的匯率可升可跌，而人民幣更受到轉換限制及外匯管制的貨幣。
</p>

