<p>8.1 除卻本公司與客戶另有協商，客戶同意本公司在2022年3月15日</p>
<p>8.2 其確認是否疑撤銷本協議授權。除非客戶於到期日前以書面指明撤銷此授權，否則此授權將會自動續期
    ，之後每次續期為期12個月。</p>
<p>8.3 本公司會於2022年2月15日日通知客戶有關續期事宜，客戶可於14天內向本公司作出撤銷續期指示，否
    則將於到時為客戶自動續期。自動續期後之協議將繼續跟隨本協議及相關之所有條款。</p>

<h3>9. 終止協議</h3>
<p>9.1 若客戶於協議期限內提前終止全權委託帳戶服務，本公司將會依照【附約II】規定收取提前終止協議手
    續費，並提前终止本協議。客戶須在提出終止要求前的七(7)個營業日內向本公司提供一份書面通知。
    如客戶決定以電話方式提出終止要求，該通話將被記錄在本公司的中央錄音系統中，作為客戶向本公司
    作出的正式終止協議指示。</p>

<p>9.2 本公司有權隨時於不少於七(7)個工作天的預先書面通知客戶，提前終止本協議。提前終止本協議概不
    影響終止當時的應計權利及義務或終止後仍然有效的明文規定。</p>
<p>9.3 若客戶投資組合淨值於協議期間內低於【附約I】規定的停損金額時，本公司將停止授權帳戶操作；在
    取得客戶重新操作授權後，始得重啟帳戶運作。</p>
<p>9.4 身故：如客戶身故(指個人或構成客戶的兩名或以上個人中的尚存者)，協議應予終止。</p>
<p>9.5 終止後果：終止當時根據本協議早已展開的一切交易並按照投資經理的慣常慣例予以完成。終止本協議
    概不影響終止當時的應計權利及義務或終止後仍然有效的明文規定。</p>

