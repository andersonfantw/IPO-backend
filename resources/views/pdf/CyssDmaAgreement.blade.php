<h3>1. 任命投資經理</h3>
<p>客戶茲任命投資經理為其代理人及非法律事務受權人，以全權委託形式把投資資產(定義見第2項條文)，
    根據本協議之條款及條文，投資及再投資於一個特設之獨立組合。投資經理謹此接受客戶任命為組合之
    投資經理。</p>


<h3>2. 投資資產</h3>
<p>2.1 「投資資產」由以下兩項構成︰</p>
<p>(a) 由客戶最初指定交予投資經理之組合之所有現金及投資，及</p>
<p>(b) 其所有投資、再投資及出售投資所得，包括但不限於投資之所有股息及利息，其升值及增額減除貶值及
    提取額。</p>
<p>2.2 在本協議中，「投資」指 (1) 股票、股份、單位信託中的單位和其他股本證券;(2)債券、票據和其
    他債務證券;(3)現貨合約和遠期合約、期權、認股權證、期貨、差價合約、掉期、外匯及衍生工具(不論
    其以何種方式與前述投資或與任何貨幣、指數或其他資產、物業資產或項目有聯繫或有關);及(4)任何種
    類之其他投資。在任何一種情況下，不論該等投資為上市或非上市、有否在任何交易所或市場買賣、屬
    私人配售或公開發售，及是否由證書或其他文件(不記名、可轉讓或其他形式)構成、作為證明或代表，
    或記入一名發行人、結算所、存管處、代管人或其他人士之簿冊，連同就上述任何一項於任何發行人、
    結算所、存管處、代管人或其他人士權利，以及與上述任何一項有關之其他權利、權益及收益。</p>

<h3>3. 投資目標及指引</h3>

<p>3.1 在不損害根據第1項條文作出之任命之概括性原則下，投資經理須對組合進行全面及綜合之全權委託形
    式管理。</p>

<p>3.2 投資經理將根據投資經理所擬的一份投資文件(「投資指引」)，包括資產類別、地域分布、目標投資
    組合的風險程度、對資產類別、市場或工具(例如使用衍生工具)的任何限制或禁止，以及表現基準(如有
    的話)投資於投資資產。投資經理保留絕對權利於其認為合適時更改投資指引，有關更改將以書面通知
    客戶。</p>

<p>3.3 為釋疑起見，(1)不得純粹因市場力量或市況變動或其他超出投資經理控制範</p>
<p>3.4 的事件令投資資產之價格或價值出現變動，而視為己違反投資指引，及(2)投資經理在管理組合時並無
    責任調查任何人士的信譽或狀況或任何文件的法律效力。</p>

<h3>4. 代管投資</h3>

<p>4.1 在適用法例及規例之規限下，任何資產管理行，包括(但不限於)中國銀盛國際證券有限公司均可以任何
    獨立戶口，包括(但不限於)綜合戶口及開戶表格登記表載列之戶口(「投資戶口」)持有投資資產。持有投
    資資產之風險由客戶單獨承擔，投資經理無須為客戶由於代管投資資產而招致的任何損失、損害、支出
    或債務而承擔責任。客戶確保投資經理在純粹因任何資產管理行或代管人之作為或不作為而招致損失時
    無須承擔債務。</p>

<p>4.2 就本4.l項條文而言，客戶須於必要時按資產管理之要求辦妥戶口開立之文件手續。</p>

<h3>5. 管理權限</h3>

<p>5.1 在不損害第3.1項條文下，投資經理有權(但無義務)在以全權方式管理組合時，以現金或保證金方式，透
    過任何資產管理或給予該資產管理授權及/或指示，買賣投資及/或持有長倉或短倉以及借入或借出證券。</p>
<p>5.2 在適用法及規例之規限下，投資經理可以於任何資產管理，包括(但不限於)資產管理行持有任何戶口，
    包括(但不限於)綜合戶口和投資戶口，並可在管理組合中指示該資產管理於該戶口進行交易。</p>
<p>5.3 在適用法例及規則之規限下，包括以最佳價格執行以及即時及公平分配之要求，投資經理可將代客戶作
    出的指令與投資經理本身的指令或其他客戶的指令匯集作出，並在投資經理認為適當下在其客戶及投資
    經理本身之間分配所購入或出售的投資。有關匯集及/或分配在某些情況下會對客戶有利，或在其他情況
    下不利於客戶。</p>
<p>5.4 客戶可以就有關本協議向投資經理發出指示。投資經理獲得授權(但無義務)按照客戶或任何授權人
    士以任何通訊方式發出的每一個指示及賴以採取行動，且投資經理有權(但無義務)要求客戶對任何指示
    作出確認。投資經理並無義務詢問或調查任何指示的真實性或真確性，也沒有義務澄清或證實任何指示
    ，而客戶須承擔因任何指示而直接或間接產生的所有風險。</p>
<p>5.5 投資經理可以在其全權絕對酌情認為適當下，任命、僱用或利用代理人、分代理人、代名人、資產管理
    、交易商、代管人及分代管人、存管處、顧問、銀行家、律師、經理和任何其聯屬公司或聯繫人士( 統稱
    為「代理人」)，委託該代理人履行投資經理對客戶之責任 (包括但不限於管理組合)，及行使投資經理於
    本協議下之權利、權力及授限，以及任何其他自投資經理與客戶不時訂立(不論是否根據本協議或其他方
    式)之進一步或其他授權、指示、權限、安排或協議。客戶接納本協議，即表示同意有關權力之轉授，並
    (在投資經理出於真誠而選擇該代理人之條件下)同意投資經理無須因該代理人之作為或不作為而導致客
    戶遭受任何損失或損害而承擔責任，除非投資經理嚴重疏忽、故意失實或欺詐，則作別論。</p>

<h3>6. 報告/投票權</h3>

<p>6.1 資產管理行將呈交詳細說明本協議規定下持有的投資及交易之組合報告。</p>
<p>6.2 投資經理將應客戶不時提出之合理要求，在收取合理費用後，向客戶提供有關投資資產所包含證券之所
    有己公開財務報表或記錄或其他報告之副本。</p>
<p>6.3 投資經理或其代理人可以在任何時候酌情就投資資產行使投票權而無須得到客戶同意，惟倘客戶作為有
    關資產的受益人以書面作出任何具體的投票指示時，投資經理或其代理人須遵從有關指示行事。</p>

<h3>7. 收費</h3>

<p>7.1 作為投資經理於本協議項下提供服務之酬勞，客戶將支付或安排支付投資經理不時指定之管理費、贖回
    費及/或表現費。首次管理及表現費乃按不時修改之收費表【附約II】記載計算及收取。</p>
<p>7.2 投資經理有權不時評估及/或修改各計劃之費用及/或計算及收取有關費用之基準。經修改之費用及/或
    計算基準於通知後的下一個收費/生效日期生效。</p>
<p>7.3 投資活動所產生的印花稅、交易徵費等外部稅項、規費，將隨香港特區政府、證監會或交易所公告或通
    知自動調整。</p>

