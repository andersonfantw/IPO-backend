    則包括完全複製/選具代表性樣本以及綜合複製，詳見下文。)
</p>

<p>以折讓或溢價交易</p>

<p>交易所買賣基金的價格可能會高於或低於其資產淨值，當中主要是供求因素的問題，在市場大幅波動
    兼變化不定期間尤其多見，專門追蹤一些對直接投資設限的市場/行業的交易所買賣基金亦可能會有
    此情況。</p>

<p>外匯風險</p>

<p>若你所買賣交易所買賣基金的相關資產並非以港幣為單位，將尚要面對外匯風險。貨幣兌換率的波動
    可對相關資產的價值造成負面影響，連帶影響交易所買賣基金的價格。</p>

<p>流通量風險</p>

<p>證券莊家是負責提供流通量、方便買賣交易所買賣基金的交易所參與者。儘管交易所買賣基金多有一
    個或以上的證券莊家，但若有證券莊家失責或停止履行職責，你或就不能進行買賣。</p>

<p>交易所買賣基金的不同複製策略涉及對手風險</p>

<p>(a) 完全複製及選具代表性樣本策略</p>

<p>採用完全複製策略的交易所買賣基金，通常是按基準的相同比重投資於所有的成份股/資產。採取選具
    代表性樣本策略的，則只投資於其中部分(而不是全部)的相關成份股/資產。直接投資相關資產而不
    經第三者所發行合成複製工具的交易所買賣基金，其交易對手風險通常不是太大問題。
</p>

<p>(b) 綜合複製策略</p>

<p>採用綜合複製策略的交易所買賣基金，主要透過掉期或其他衍生工具去追蹤基準的表現。現時，採取綜
    合複製策略的交易所買賣基金可再分為兩種：</p>

<p>i. 以掉期合約構成</p>

<p>總回報掉期讓交易所買賣基金經理可以複製基金基準的表現而不用購買其相關資產。</p>

<p>以掉期合約構成的交易所買賣基金需承受源自掉期交易商的交易對手風險。若掉期交易商失責或不能履
    行其合約承諾，基金或要蒙受損失。</p>

<p>ii. 以衍生工具構成</p>

<p>交易所買賣基金經理也可以用其他衍生工具，綜合複製相關基準的經濟利益。有關衍生工具可由一個
    或多個發行商發行。</p>

<p>以衍生工具構成的交易所買賣基金需承受源自發行商的交易對手風險。若發行商失責或不能履行其合
    約承諾，基金或要蒙受損失。</p>

<p>交易所買賣基金即使取得抵押品，也需依靠抵押品提供者履行責任。此外，申索抵押品的權利一旦行
    使，抵押品的市值可以遠低於當初所得之數，令交易所買賣基金損失嚴重。</p>

<h3>5. 期貨及期權交易的風險</h3>

<p>買賣期貨合約或期權之虧蝕風險可以極大。在若干情況下，客戶所蒙受之虧蝕可能會超過客戶最初存入
    之保證金數額。即使客戶設定了備用指示，例如「止蝕」或「限價」等指示，亦未必能夠避免損失。
    市場情況可能使該等指示無法執行。客戶可能會在短時間內被要求存入額外之保證金。假設未能在指
    定之時間內提供所需數額，客戶之未平倉合約可能會被平倉，而客戶仍然要對戶口內任何因此而出現
    之短欠數額負責。因此，客戶在買賣前應研究及理解期貨合約及期權，以及根據客戶本身之財政狀況
    及投資目標，仔細考慮這種買賣對客戶是否適合。假如客戶買賣期權，便應熟悉行使期權及期權到期
    時的程序，以及客戶在行使期權及期權到期時的權利及責任。</p>

<h3>6. 槓桿式外匯交易的風險</h3>

<p>槓桿式外匯交易的虧損風險可以十分重大。你所蒙受的虧損可能超過你的最初保證金款額。即使你定下
    備用交易指示，例如 "止蝕" 或 "限價" 交易指示，亦未必可以將虧損局限於你原先設想的數額。市
    場情況可能使這些交易指示無法執行。你可能被要求一接到通知即存入額外的保證金款額。如你未能在
    所訂的時間內提供所需的款額，你的未平倉合約可能會被了結。你將要為你的帳戶所出現的任何逆差負
    責。因此，你必需仔細考慮，鑑於自己的財務狀況及投資目標，這種買賣是否適合你。</p>

<h3>7. 保證金買賣的風險</h3>

<p>藉存放抵押品而為交易取得融資的虧損風險可能極大。你所蒙受的虧蝕可能會超過你存放於有關持牌人
    或註冊人作為抵押品的現金及任何其他資產。市場情況可能使備用交易指示，例如"止蝕" 或 "限價"
    指示無法執行。你可能會在短時間內被要求存入額外的保證金款額或繳付利息。假如你未能在指定的
    時間內支付所需的保證金款額或利息，你的抵押品可能會在未經你的同意下被出售。此外，你將要為你
    的帳戶內因此而出現的任何短欠數額及需繳付的利息負責。因此，你應根據本身的財政狀況及投資目標，
    仔細考慮這種融資安排是否適合你。</p>

